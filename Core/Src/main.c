bool tud_vendor_control_request_cb(uint8_t rhport,
		tusb_control_request_t const *request) {
	switch (request->bRequest) {
	case VENDOR_REQUEST_WEBUSB:
		// match vendor request in BOS descriptor
		// Get landing page url
		return tud_control_xfer(rhport, request, (void*) &desc_url,
				desc_url.bLength);

	case 0x22:
		// Webserial simulate the CDC_REQUEST_SET_CONTROL_LINE_STATE (0x22) to
		// connect and disconnect.
		web_serial_connected = (request->wValue != 0);

		// Always lit LED if connected
		if (web_serial_connected) {
			board_led_write(true);
			xTimerChangePeriod(usbled_timerHandle,
					pdMS_TO_TICKS(BLINK_ALWAYS_ON), 0);

			tud_vendor_write_str("\r\nTinyUSB WebUSB device example\r\n");
		} else {
			xTimerChangePeriod(usbled_timerHandle, pdMS_TO_TICKS(BLINK_MOUNTED),
					0);
		}

		// response with status OK
		return tud_control_status(rhport, request);

	default:
		// stall unknown request
		return false;
	}

	return true;
}

